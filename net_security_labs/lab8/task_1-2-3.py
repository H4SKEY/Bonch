from utils import mod_inverse, mod_pow

# --- Исходные данные (Вариант 7) ---
p = 53
q = 59
m = 4

print("--- Лабораторная работа 8: RSA ---")
print(f"Исходные данные: p={p}, q={q}, m={m}")

# --- Задание 1: Формирование первого ключа (Открытого) ---

# 1. Вычисление модуля n
n = p * q
print(f"\n1. Модуль n = p * q = {n}")

# 2. Вычисление функции Эйлера phi(n)
phi_n = (p - 1) * (q - 1)
print(f"   Функция Эйлера phi(n) = {phi_n}")

# 3. Выбор открытой экспоненты e
e = 17

from math import gcd
if gcd(e, phi_n) != 1:
    raise ValueError(f"Выбранное e={e} не взаимно просто с phi(n)={phi_n}. Выберите другое e.")

print(f"   Выбран открытый ключ e = {e}")
print(f"   Открытый ключ (e, n) = ({e}, {n})")

# --- Задание 2: Определение второго ключа (Закрытого) ---

# Вычисление закрытой экспоненты d: d = e^-1 mod phi(n)
d = mod_inverse(e, phi_n)
print(f"\n2. Закрытый ключ d = e^-1 mod phi(n) = {d}")
print(f"   Закрытый ключ (d, n) = ({d}, {n})")

# Проверка: e * d mod phi(n) должно быть равно 1
check = (e * d) % phi_n
print(f"   Проверка (e * d) mod phi(n) = {check} (должно быть 1)")

# --- Задание 3: Шифрование и расшифрование ---

# 1. Шифрование: c = m^e mod n
c = mod_pow(m, e, n)
print(f"\n3. Шифрование:")
print(f"   Исходное сообщение m = {m}")
print(f"   Шифротекст c = m^e mod n = {m}^{e} mod {n} = {c}")

# 2. Расшифрование: m_dec = c^d mod n
m_dec = mod_pow(c, d, n)
print(f"\n   Расшифрование:")
print(f"   Восстановленное сообщение m' = c^d mod n = {c}^{d} mod {n} = {m_dec}")

# Итоговая проверка
if m_dec == m:
    print("\nРЕЗУЛЬТАТ: Расшифрование успешно. Сообщение восстановлено верно.")
else:
    print("\nРЕЗУЛЬТАТ: ОШИБКА. Сообщение не восстановлено.")